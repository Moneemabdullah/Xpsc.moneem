/*  --------------------SOLUTION PROGRAM-------------------------*/
int gcd_of_vector(
    vector<int> &v)
{
    int result = v[0];
    for (int i = 1; i < v.size(); i++)
    {
        result = gcd(result, v[i]);
    }
    return result;
}

void solve()
{
    int n;
    cin >> n;

    vector<int> a(n), b(n);

    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++)
    {
        cin >> b[i];
    }

    int gcd_a = gcd_of_vector(a);
    int gcd_b = gcd_of_vector(b);
    int initial_sum = gcd_a + gcd_b;

    int max_gcd_sum = initial_sum;
    int max_count = 0;

    for (int l = 0; l < n; l++)
    {
        for (int r = l; r < n; r++)
        {

            vector<int> new_a = a;
            vector<int> new_b = b;

            for (int i = l; i <= r; i++)
            {
                swap(new_a[i], new_b[i]);
            }

            int gcda = gcd_of_vector(new_a);
            int gcdb = gcd_of_vector(new_b);
            int new_sum_gcd = gcda + gcdb;

            if (new_sum_gcd > max_gcd_sum)
            {
                max_gcd_sum = new_sum_gcd;
                max_count = 1;
            }
            else if (new_sum_gcd == max_gcd_sum)
            {
                max_count++;
            }
        }
    }

    cout << max_gcd_sum << " " << max_count << endl;
}
