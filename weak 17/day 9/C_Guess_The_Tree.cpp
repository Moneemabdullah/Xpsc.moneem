const int N = 1004;
bool chack[N];
int par[N];

int ask(int u, int v)
{
    cout << "? " << u << " " << v << endl;
    cout.flush();
    int ans;
    cin >> ans;
    return ans;
}

void path(int u, int v)
{
    int x = ask(u, v);
    if (x == u || x == v)
    {
        par[v] = u;
        chack[v] = true;
        return;
    }

    if (!chack[x])
    {
        path(u, x);
    }
    path(x, v);
}

void solve()
{
    int n;
    cin >> n;
    for (int i = 1; i <= n; i++)
    {
        chack[i] = false;
    }

    chack[1] = true;
    for (int i = 2; i <= n; i++)
    {
        if (!chack[i])
        {
            path(1, i);
        }
    }

    cout << "! ";
    for (int i = 2; i <= n; i++)
    {
        cout << i << " " << par[i] << " ";
    }
}
